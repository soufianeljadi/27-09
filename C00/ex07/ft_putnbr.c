#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int	a;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1 ;
	}
	a = nb;
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	else
	{
		a = a + 48;
		write(1, &a, 1);
	}
}
